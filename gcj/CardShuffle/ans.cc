#include <iostream>
#include <vector>
using namespace std;

int solve(int M, int C, int W, vector<int>& A, vector<int>& B) {
    for (int i = C - 1; i >= 0; i--) {
        //cout << W << " " << A[i] << " " << B[i] << endl;
        // Wの位置をずらしていく!!
        
        // 一つ前のシャッフルで上に持ってこられたのなら
        // もとにもどしてやる.元の位置は1 -> Ai, 2 -> Ai+1...なので
        // 1 + Ai - 1 , 2 + Ai - 1, ..., W + Ai - 1
        if (W <= B[i]) {
            W += A[i] - 1;
        }
        // 一つ前のシャッフルでスライスされるところより上にいたなら
        // -Bしてやることで上にあげてやる
        else if (W <= A[i] + B[i] - 1) {
            W -= B[i];
        }
        // else { }
        // Wよりも上でシャッフルしてもWには影響なし
    }
    return W;
}

int main() {
    int T, M, C, W, tmpA, tmpB;
    cin >> T;
    for (int i = 1; i <= T; i++) {
        cin >> M >> C >> W;
        vector<int> A;
        vector<int> B;
        for (int j = 0; j < C; j++) {
            cin >> tmpA >> tmpB;
            A.push_back(tmpA);
            B.push_back(tmpB);
        }
        int answer = solve(M, C, W, A, B);
        cout << "Case #" << i << ": " << answer << endl;
    }
}

